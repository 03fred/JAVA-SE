package br.com.numeros.literais;

public class NumerosLiterais {

	public static void main(String[] args) {
		
		System.out.println();
		System.out.println("inteiro decimal:\t\t" + 11);
		System.out.println("inteiro hexadecimal:\t\t" + 0xb);
		System.out.println("inteiro octa:\t\t\t" + 013);
		System.out.println("inteiro longo:\t\t\t" + 11L);
		System.out.println("real precisão simples:\t\t" + 24.2f);
		System.out.println("real precisão dupla:\t\t" + 24.2);
		System.out.println("tipo lógico:\t\t\t" + true);
		System.out.println("tipo caractere:\t\t\t" + 'h');
		System.out.println("text:\t\t\t\t" + "ANA");
		System.out.println();


		
		
	}

}
